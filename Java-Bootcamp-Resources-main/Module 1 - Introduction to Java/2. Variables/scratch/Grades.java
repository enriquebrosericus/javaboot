public class Grades {

    public static void main(String[] args) {
        char tacoGrade = 'A';
        char burritoGrade = 'B';
        char enchiladaGrade = 'C';

        System.out.println("Taco Grade: " + tacoGrade);
        System.out.println("Burrito Grade: " + burritoGrade);
        System.out.println("Enchilada Grade: " + enchiladaGrade);

//tell the student the overall grade
        System.out.println("Overall Grade: " + tacoGrade + burritoGrade + enchiladaGrade);

    }
}